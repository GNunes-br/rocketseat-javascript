/*
    Operadores Aritiméticos
*/

//Multiplicação (*)
console.log('Multiplicação ' + 3 * 5)
//Divisão (/)
console.log('Divisão ' + 25 / 5)
//Soma (+)
console.log('Soma ' + (25 + 5))
//Subtração (-)
console.log('Subtração ' + (25 - 5))
//Resto da divisão
console.log('Módulo ' + 25 % 4)
//Incremento
let increment = 0
increment++
console.log('Incremento ' + increment)
//Decremento
let decrement = 1
decrement--
console.log('Decremento ' + decrement)
//Exponencial
console.log('Exponencial ' + (3**3))
